-- 更新品质术语：凡级→凡品、仙级→仙品、神级→神品
-- 执行日期：2025-01-XX
-- 用途：将已有数据库中的品质术语从"级"更新为"品"

-- ============================================
-- 更新灵兽模板表的品质字段
-- ============================================

UPDATE spirit_beast_templates
SET quality = '凡品'
WHERE quality = '凡级';

UPDATE spirit_beast_templates
SET quality = '仙品'
WHERE quality = '仙级';

UPDATE spirit_beast_templates
SET quality = '神品'
WHERE quality = '神级';

